<h3>Statut zajednice <a href="http://webserveri.info/RP/?akcija=statut">WebServeri</a></h3>
<p>U skladu sa odredbama čl.11. i 12. Zakona o udruženjima (»Službeni list RS«, br.51/09), na osnivačkoj skupštini koja <b>je održana usmenim dogovorom,</b> usvojen je statut udruženja:</p>
<h3>» Udruženje korisnika Webserveri «</h3>
<p>Oblast ostvarivanja ciljeva</p>
<p><span>Član 1.</span></p>
<p><em>» Udruženje korisnika Webserveri «</em> je nevladino i neprofitno udruženje, osnovano na neodređeno vreme radi ostvarivanja ciljeva oko obrazovanja, širenja informatičke kulture, slobodnog softvera i informacionih tehnologija. Licenca je potpuno otvorena, te ne postoji nikakvo pravno potraživanje od strane founder-a <b>(hightech)</b>, succesor-a <b>(ZDroid)</b> i admin tima <b>(vladap, ludus, nemysis, kgb)</b>.</p>
<h3>« Ciljevi udruženja »</h3>
<p><span>Član 2.</span></p>
<p>Ciljevi Udruženja su: popularizacija otvorenih sistema, edukacija i podizanje opšte svesti o upotrebi i korišćenju slobodnog softvera u edukativne, profesionalne i amaterske svrhe. Organizovanje neformalnih i formalnih druženja linux korisnika kao i predavanja na temu upotrebe slobodnog softvera. Potpuno otvaranje koda i omogućavanje običnom korisniku da koristi sisteme otvorenog koda</p>
