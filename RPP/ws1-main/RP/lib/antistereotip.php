<b>Anti</b><em>Stereotip</em> je pre svega način razmišljanja... Projekat je otvorenog koda <span>(open source)</span> i svi ste pozvani u razvoj...<br /> 
Postoje predrasude i stereotipi, stoga su i kreirani standardi. Svi učestvuju u standardu, stereotipu ili predrasudi - posredno ili neposredno, <em>time potpomažući ovaj nakaradni sistem ...</em> <br />
Potreban je način razmišljanja koj će razbiti <span>STEREOTIPE</span>. Dobrodošli u svet digitalne percepcije i otvorenog koda...<br />
Cilj <b>AntiStereotipa</b> je razbijanje predrasuda u globalnom smislu! 
</p>
<p style="background: #313131;">
AntiStereotip je <span>BEZ CENZURE</span> na praktičnu primenu u ovom slučaju tehnologije na nivou saznavanja činjenica kao korisnih informacija. Kompletan razvoj AntiStereotipa kao kompjuterskog koda kreiran je zahvaljujući sistemima i alatima otvorenog koda: <b>GNU/Linux, UNIX, PHP, HTML5/CSS3, XML, Nginx, Apache2, Py, Perl, C, LISP ... </b> Detaljnije o pomenutim tehnologijama možete naći na <a href="../forum">forumu</a> ...
