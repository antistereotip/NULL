<p>
<b>WebServer1(WS1)</b> je projekat <b>otvorenog koda</b>, baziran na efikasnoj podršci, vanstandardnom dizajnu, brzini, sigurnosti ...<br />
Zajednica predstavlja skup ljudi koji entuzijazmom i radom promovišu <b>otvoreni kod</b> u domenu zajedničkog znanja i interesovanja. Dinamička strana našeg sajta je forum koji između ostalog nije samo forum već i baza znanja.<br />
