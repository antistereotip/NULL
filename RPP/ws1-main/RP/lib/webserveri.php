Neke od delatnosti koje <span>WS1 zajednica</span> obuhvata su: <em>Unix, GNU/Linux, Serveri, Programiranje, Dizajn, Web razvoj</em>...<br /> 
Naša ideja je da običnom korisniku olakšamo i približimo sastave <b>otvorenog koda</b> kao i da ponudimo podršku u sferi našeg znanja.<br /> 
