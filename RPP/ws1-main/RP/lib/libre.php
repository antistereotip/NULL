<p>
<span>LiBRE!</span> je nekomercijalni časopis o slobodnom softveru na srpskom jeziku iza koga stoji grupa entuzijasta okupljenih oko jedne ideje. Ta ideja podrazumeva nesebično deljenje svoga znanja u cilju približavanja <b>slobodnog softvera</b> običnim ljudima koji nisu programeri ali takođe obezbeđuje i izvesnu količinu informacija namenjenu programerima i naprednijim korisnicima računara. Cilj ovog časopisa je da dostigne ugled kod domaće (a delom i strane) publike i da bude jedan od omiljenih izvora informisanja u svetu slobodnog softvera.
</p>
<p>
<em>LiBRE! časopis o slobodnom softveru</em> izlazi jednom mesečno na srpskom jeziku i to na oba pisma, latiničnom i ćiriličnom. Trenutno je časopis dostupan samo u elektronskoj varijanti u obliku pdf dokumenta i potpuno je besplatan za preuzimanje.
</p>
<p>
Članovi <b>LiBRE!</b> tima sarađuju međusobno putem interneta. Jednom nedeljno održavaju sastanak na IRC-u. Svaki član projektnog tima učestvuje u ovom projektu dobrovoljno. Novi članovi projektnog tima su uvek dobrodošli. Ukoliko vam ideja o nekomercijalnom časopisu o slobodnom softveru na srpskom jeziku zvuči zanimljivo, slobodno možete da nam se pridružite.
</p>

