<p>Mnogi korisnici kompjutera svakodnevno koriste modifikovanu verziju <a href="http://www.gnu.org/philosophy/categories.html#TheGNUsystem">GNU sistema (18k 
teksta)</a>, a da to i ne znaju. Kao posledica zanimljivog razvoja događaja, verzija GNU-a poznatija
kao &quot;Linux&quot; se danas mnogo koristi, te mnogi korisnici nisu
svesni u kojoj je meri Linux povezan s <a href="http://www.gnu.org/gnu/thegnuproject.bs.html">GNU Projektom</a>.</p> 
<p>Zaista postoji Linux; to je kernel i ovi ljudi ga koriste. Ali kernel sam po sebi se ne
može koristiti; kernel je koristan samo kao deo celokupnog operativnog sistema. Linux se obično
koristi u kombinaciji sa GNU operativnim sistemom: sistem u osnovi je GNU sa Linux-om koji 
ima funkciju kernela.</p> 
<p>Mnogi korisnici nisu potpuno svesni razlike između kernela, a to je Linux, i čitavog
sistema, koji takođe nazivaju &quot;Linux&quot;. Dvosmisleno korištenje imena ne doprinosi
razumijevanju.</p> 
<p>Programeri uopšteno znaju da je Linux kernel. Ali pošto su čuli kako se čitav
sistem naziva &quot;Linux&quot;, oni obično prikažu istoriju koja odgovara tom imenu. Na
primer, mnogi veruju da kada je Linus Torvalds završio pisanje kernela, njegovi
prijatelji su počeli tražiti drugi besplatan software, te bez nekog posebnog razloga je
skoro sve potrebno da se napravi Unixoidni sistem već bilo dostupno.</p> 
<p>Ono što su pronašli nije bila slučajnost--bio je to GNU sistem. Postojeći
<a href="http://www.gnu.org/philosophy/free-sw.bs.html">slobodan software</a> je upotpunio kompletan sistem zato
što je GNU Projekat radio na njemu još od 1984. 
<a href="http://www.gnu.org/gnu/manifesto.html">The GNU Manifesto (31k teksta)</a> postavlja
za cilj razvoj besplatnog Unixoidnog sistema, zvanog GNU. <a 
href="http://www.gnu.org/gnu/initial-announcement.html">Inicijalna obavijest</a> GNU
projekta prikazuje neke od originalnih planova za GNU sistem. U doba kada je Linux pisan, 
sistem je bio gotovo završen.</p> 
<p>Većina projekata slobodnog software-a ima za cilj razvoj određenog programa za rešavanje određenog zadatka. Na
primer, Linus Torvalds je odlučio da napiše Unixoidni kernel (Linux); Donald Knuth je
odlučio da napiše text formater (TeX); Bob Scheifler je odlučio da razvije sistem
prozora (<a href="http://www.camb.opengroup.org/tech/desktop/x/">X Windows</a>). Sasvim je
prirodno meriti doprinos ovakve vrste projekta putem specifičnih programa koji su
nastali kao rezultat projekta.</p> 
<p>Ako bi želeli na ovaj način izmeriti doprinos GNU Projekta, šta bismo trebali
razmatrati? Jedan distributer CD-ROM-ova je otkrio da u njihovoj &quot;Linux distribuciji&quot;, <a 
href="http://www.gnu.org/philosophy/categories.html#GNUsoftware">GNU software</a> zauzima
najviše mesta, oko 28% čitavog izvornog koda, a ovo je uključivalo i neke osnovne komponente bez
kojih ne bi moglo biti sistema. Sam Linux je bio oko 3%. Dakle, ako biste birali ime za sistem
na osnovu toga ko je napisao programe u sistemu, najprikladniji izbor bio bi
&quot;GNU&quot;.</p>
<p>Ali mi ne smatramo da je to pravi način da razmatranje problema. GNU Projekat nije bio,
niti je sada projekat za razvoj posebnih software paketa. To nije projekt za razvoj <a 
href="http://www.gnu.org/software/gcc/gcc.html">C kompajlera (compiler)</a>, iako smo i to
uradili. Ovo nije projekat za razvoj text editora, iako smo razvili jedan takav. Cilj GNU
Projekta je razvoj <em>celokupnog besplatnog Unixoidnog sistema</em>.</p> 
<p>Mnogi ljudi su dali značajan doprinos sistemu slobodnog software-a, te svi oni zaslužuju
pohvale. Ali razlog zbog kojega je to <b>sistem</b>--a ne samo kolekcija korisnih
programa--leži u tome što je GNU Projekat odlučio da ga učini sistemom.
Napravili smo listu programa potrebnih za izradu <em>potpuno</em> besplatnog sistema, te
smo ih pronalazili, pisali sami ili pronašli ljude da napišu sve na listi. Napisali smo osnovne ali
nezanimljive glavne delove, kao asembler i linker, jer ne možete imati sistem bez njih.
Celokupan sistem treba više od programerskih alata; Bourne Again SHell, Post Script
prevodilac <a href="http://www.gnu.org/software/ghostscript/ghostscript.html"><b>Ghostscript</b></a>,
kao i
<a href="http://www.gnu.org/software/libc/libc.html">GNU C biblioteke</a> su jednako važni.</p> 
<p>Do ranih devedesetih sastavili smo čitav sistem osim kernela (takođe smo radili i na
kernelu <a href="http://www.gnu.org/software/hurd/hurd.html">GNU Hurd</a>, koji radi
povrh Mach kernela). Razvoj kernela je išao teže nego što smo pretpostavljali, te još <a 
href="http://www.gnu.org/software/hurd/hurd-and-linux.html">radimo na tome da ga
završimo</a>.</p>
<p>Na sreću, vi ne morate čekati na to, jer Linux već radi. Kada je Linus Torvalds
napisao Linux, popunio je zadnju glavnu rupu. Ljudi su zatim mogli da spoje Linux sa GNU
sistemom da bi napravili cjelokupan besplatan sistem: GNU sistem baziran na Linux-u (ili
kraće GNU/Linux sistem).</p> 
<p>Ovo sastavljanje zvuči jednostavno, ali to nije bio trivijalan posao. <a 
href="http://www.gnu.org/software/libc/libc.html">GNU C biblioteka</a> (koja se kraće naziva
glibc) je trebala suštinske izmene. Integrisanje kompletnog sistema u distribuciju koja
treba da funkcioniše &quot;iz kutije&quot; (dakle, odmah po instalaciji) je bio veliki zadatak. Zahtevalo je rešavanje
problema kako instalirati i podignuti sistem - problem koji još nismo bili dotakli, jer
nismo bili stigli do te tačke. Ljudi koji su razvili razne distribucije sistema su mnogo
doprinijeli.</p> 
<p>Pored GNU-a, još jedan projekat je neovisno proizveo besplatan Unixoidni operativni
sistem. Ovaj sistem je poznat kao <a 
href="http://www.gnu.org/links/links.html#OtherFreeSoftware">BSD</a>, i razvijen je na UC
Berkeley. BSD programeri su bili inspirisani da naprave slobodan sistem, kao što je GNU
Projekat, i povremeno su bili ohrabrivani od strane GNU aktivista, ali njihov projekat ima malo
zajedničkih tačaka sa GNU konceptom. Danas BSD sistemi delimično koriste GNU software, isto kao što GNU
sistem i njegove varijante koriste dio BSD software-a; ali uzimajući u cjelini, oni su
dva odvojena sistema koji su odvojeno evoluirali. Bilo koji slobodan operativni sistem koji 
postoji danas je gotovo sigurno ili varijanta GNU sistema ili neka vrsta BSD sistema.</p> 
<p>GNU Projekat podržava GNU/Linux sisteme kao i <em>sam</em> GNU sistem--čak i pomoću
donacija. Sponzorisali smo preprogramiranje proširenja za GNU C biblioteke vezanih za Linux, tako da
su sada dobro intergrisane, te ih najnoviji GNU/Linux sistemi koriste bez ikakvih
promena. Takođe smo sponzorisali razvoj <a href="http://www.debian.org/">Debian
GNU/Linux-a</a>.</p> 
<p>Danas koristimo Linux bazirane GNU sisteme za većinu rada, te se nadamo da ih i vi
takođe koristite. <b>Ali molimo vas da ne zbunjujete javnost koristeci ime
&quot;Linux&quot; dvosmisleno. Linux je kernel, jedan od osnovnih dijelova sistema. Sistem
u celini je više ili manje GNU sistem.</b></p> 
