<p>
Zelimo da vam predstavimo <em>Gnome3</em> okruženje pokrenuto na Debain Wheezy (testing verziji) . <br />
GNOME 3 donosi ozbiljne promene, kako unutra tako i spolja. 
Iz korisničkog ugla najvažnija promena je GNOME Shell, 
poptuno drugačija organizacija radne površine i manipulacije prozorima. 
Uz GNOME 3 dolazi i GTK 3, nova verzija toolkita na kojem je GNOME izgrađen.<br />
GNOME Shell, kao novina u Gnome-u je popuno nov aspekt kada je desktop u pitanju. Nešto nalik plazmi u KDE okruženju.
Sve što ste do sada znali o Gnome 2 okruženju i sve na šta ste navikli slobodno zaboravite, jer tu je nova 
generacija okruženja.
Zanimljiva alatka za "tweak"-ovanje Gnome 3 okruženja je <em>gnome-tweak-tool</em> sa kojom lako mozete prilagoditi
okruzenje vasim potrebama.
Dosta je dobro prilagodjen hardverskim zahtevima, zapravo ima vecu podrsku i bolju integraciju sa hardverskim resenjima.<br />
Zanimljiva stvar takodje je kompitabilnost sa mreznim uredjajima... Bilo koj telefon sa android OS (testirano) moze lako 
bez podesavanja biti odmah prepoznat kao AP - sve sto treba da ukljucite je opcija USB tethering i uredjaj je sto bi se
reklo "PLUG-AND-PLAY"...

<br />

Ja licno kao autor teksta ne znam puno oko Gnome3 okruzenja, osim da ga podesim. 
Testirao sam ga na vise racunara i dobro radi, bez greske, kao da vozite mercedes :) ...
Lakse je od <b>gnome2</b> okruzenja 30% - 40% procenata, bar u ovom slucaju ...<br />
Na slici ispod mozete videti neke od performasi neophodnih UI, UX, WEB dizajnerima i razvojnim timovima, kao i pojedincu sklonom eksperimentima ...  
