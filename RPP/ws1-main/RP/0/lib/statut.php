<h2><a href="http://webserveri.info/?akcija=statut">Statut zajednice Webserveri</a></h2>
<p><i>U skladu sa odredbama čl.11. i 12. Zakona o udruženjima (»Službeni list RS«, br.51/09), na osnivačkoj skupštini koja <b>nije održana niti će biti (održana je u našim glavama),</b> usvojen je statut udruženja:</i></p>
<h3>« Udruženje korisnika Webserveri »</h3>
<p>Oblast ostvarivanja ciljeva</p>
<p>Član 1.</p>
<p>« Udruženje korisnika Webserveri » je nevladino i neprofitno udruženje, osnovano na neodređeno vreme radi ostvarivanja ciljeva oko obrazovanja, širenja informatičke kulture, slobodnog softvera i informacionih tehnologija. Licenca je potpuno otvorena, te ne postoji nikakvo pravno potraživanje od strane founder-a <b>(hightech)</b>, succesor-a <b>(ZDroid)</b> i admin tima <b>(vladap, ludus, nemysis, kgb)</b>.</p>
<h3>Ciljevi udruženja</h3>
<p>Član 2.</p>
<p>Ciljevi Udruženja su: popularizacija otvorenih sistema, edukacija i podizanje opšte svesti o upotrebi i korišćenju slobodnog softvera u edukativne, profesionalne i amaterske svrhe. Organizovanje neformalnih i formalnih druženja linux korisnika kao i predavanja na temu upotrebe slobodnog softvera. Potpuno otvaranje koda i omogućavanje običnom korisniku da koristi sisteme otvorenog koda</p>

