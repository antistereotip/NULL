<h2>UNIX</h2>
<p>
<b>The Open Group</b> holds the definition of what a UNIX system is and its
associated trademark in trust for the industry. 
</p>
<p>
In 1994 Novell (who had acquired the UNIX systems business of AT&T/USL)
decided to get out of that business. Rather than sell the business as a
single entity, Novell transferred the rights to the UNIX trademark and the
specification (that subsequently became the Single UNIX Specification)
to The Open Group (at the time X/Open Company). Subsequently, it
sold the source code and the product implementation (UNIXWARE)
to SCO. The Open Group also owns the trademark UNIXWARE.
</p>
<p>
Today, the definition of UNIX &reg; takes the form of the worldwide
Single UNIX Specification integrating X/Open
Company's XPG4, IEEE's POSIX Standards and ISO C. Through continual evolution,
the Single UNIX Specification is the defacto and dejure
standard definition for the UNIX system application programming 
interfaces.
As the owner of the UNIX trademark, The Open Group has separated the
UNIX trademark from any actual code stream itself, thus allowing multiple
implementations. Since the introduction of the Single UNIX Specification,
there has been a single, open, consensus specification that defines the
requirements for a conformant UNIX system.
</p>
<p>
There is also a mark, or brand, that is used to identify those products
that have been certified as conforming to the Single UNIX Specification,
initially UNIX 93, followed subsequently by UNIX 95, UNIX 98 and now 
UNIX 03. 
</p>
<p>
The Open Group is committed to working with the community to further the
development of standards conformant systems by evolving and maintaining
the Single UNIX Specification and participation in other related
standards efforts. Recent examples of this are making the standard
<a href="http://www.unix.org/online.html">freely available on the web</a>, 
permitting reuse of the standard in 
<a href="http://www.opengroup.org/comm/press/21jan04.htm">
open source documentation projects</a> ,
providing <a href="http://www.opengroup.org/testing/downloads.html">test tools</a> ,developing the <a href="http://www.posixcertified.ieee.org/">POSIX</a>
and <a href="http://www.opengroup.org/lsb/cert/">LSB</a> certification
programs.
</p>
<p>From this page you can read about the history of the UNIX system over the
past 40 years or more. 
You can learn about the Single UNIX Specification, and read or download
online versions of the specification.
You can also get involved in the ongoing development and maintenance
of the Single UNIX Specification, by joining 
<a href="http://www.opengroup.org/austin/">the Austin Group</a> whose
approach to specification development is "<i>write once, adopt everywhere</i>", 
The Open Group's <a href="http://www.opengroup.org/platform/">Base Working Group</a> or
get involved in the <a href="http://www.opengroup.org/platform/unix_certification/">UNIX Certification program</a>.</p>
