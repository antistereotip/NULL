<h3><a href="http://webserveri.info/">www.webserveri.info</a></h3>

<p><b>www.webserveri.info</b> je projekat otvorenog koda, baziran na efikasnoj "live" podršci. Zajednica predstavlja skup ljudi koji entuzijazmom i radom promovišu otvoreni kod u domenu zajedničkog znanja i interesovanja. Dinamička strana našeg sajta je forum koji između ostalog nije samo forum već i baza znanja, a on se nalazi na adresi <a href="http://webserveri.info/forum">http://webserveri.info/forum</a><br />
Neke od delatnosti koje zajednica obuhvata su: <b>Unix, GNU/Linux, Serveri, Programiranje, Dizajn, Web razvoj...</b> <br />
Naša ideja je da običnom korisniku olakšamo i približimo sastave otvorenog koda kao i da ponudimo podršku u sferi našeg znanja.<br />
Ali kao što i samo ime sajta kaže, pretežno stavljamo akcenat na servere i na njihovu implementaciju u sisteme otvorenog koda.<br />
