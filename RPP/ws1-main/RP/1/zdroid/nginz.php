<h2>[Engine~Z ®]</h2>
<h3>MOTOR~Z ®</h3>
<p>
UVOD<br />
----<br />
Zdravo i dobrodošli u <b>nginZ.</b><br />
<span>NginZ</span> je skraćenica od Engine~Z. To je lightweight web site engine.<br />
Šta je moja ideja? Jednostavan engine za web stranice sa dobrim izgledom/dizajnom.
</p>

<p>
TEHNOLOGIJE<br />
-----------<br />
<em>Engine~Z</em> je napisan u HTML-u 5, CSS-u 3 i PHP-u. Ekstenzija nekih datoteka je .php, ali većina koda je HTML (pod PHP-om).
</p>

<p>
GENERALNO<br />
---------<br />
Projekat je objavljen kao open source software (OSS) pod uslovima <b>PostgreSQL licence</b> (pogledajte LICENSE datoteku). Za više informacija o OSS-u pogledajte <a href="http://opensource.org/">http://opensource.org/</a>.
</p>

<p>
UKLjUČITE SE<br />
------------<br />
Vi mi možete pomoći forkovanjem ovog repoa i testiranjem.<br />
* Pretražiću sve forkove i pogledati promene (commit-e). Sve ispravke i zakrpe su dobrodošle.<br />
* Ne menjajte master razvojnu granu! To je stabilna i zvanična razvojna grana. Za vaše zakrpe/ispravke napravite novu razvojnu granu, kao patch-1 ili html-fix.<br />
* 2. način za promenu datoteka: kliknite na edit/add dugme u mom repo-u i kada ga promenite poslaćete pull request i napraviti novu razvojnu granu kao patch-1.<br />
* Za bilo koje predloge i (beta/alpha) testiranje možete me kontaktirati putem email-a (pogledajte AUTOR). Takođe možete koristiti Issues stranicu za poboljšanja.<br />
* Pre postavljanja issue-a, pretražite Google i pogledajte Wiki - https://github.com/ZDroid/nginZ/wiki. Takođe, pretražite postojeće probleme.
</p>

